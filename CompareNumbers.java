public class CompareNumbers {
    public static void main(String[] args) {
        int n1 = 10, n2 = 20;
        if (n1 > n2)
            System.out.println("n1 is greater");
        else if (n1 < n2)
            System.out.println("n2 is greater");
        else
            System.out.println("Both are equal");
    }
}
